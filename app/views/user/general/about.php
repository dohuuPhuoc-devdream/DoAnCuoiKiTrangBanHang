    <h2 class="section-title">Khởi đầu</h2>
    <p>Năm 2016, sau khi thành công vang dội với thương hiệu thời trang nam L’JIN, founder là ông Nguyễn Trọng Lâm đã
        chính thức thành lập thương hiệu mới mang tên LSOUL vào năm 2017, cũng là lần đầu LSOUL xuất hiện tại thị trường
        Việt Nam với mô hình kinh doanh sản phẩm nhập khẩu. Các sản phẩm được lựa chọn và nhập trực tiếp bởi chính
        founder của thương hiệu, dựa trên tiêu chí đáp ứng về phong cách khác biệt – chất lượng đảm bảo – xu hướng đón
        đầu, phục vụ nhu cầu mua sắm của khách hàng nữ tại thị trường thời trang Việt Nam lúc bấy giờ. Cửa hàng ban đầu
        còn khá thô sơ và quy mô nhỏ, toàn bộ đều được lên ý tưởng và decor thủ công bởi chính founder.</p>
    <h2 class="section-title">Chuyển mình ngoạn mục</h2>
    <p>Năm 2022 đánh dấu bước ngoặt lịch sử khi LSOUL chính thức chuyển mình trở thành thương hiệu thiết kế chuyên
        nghiệp. Với sự sáng tạo độc đáo và cách tiếp cận đổi mới, LSOUL đã nhanh chóng thu hút sự chú ý của giới mộ điệu
        thời trang quốc tế. Lấy cảm hứng từ những quan sát tinh tế về xu hướng thời trang, LSOUL không chỉ dự đoán mà
        còn dẫn đầu trong việc tạo dựng xu hướng và phong cách thời trang mới.</p>
