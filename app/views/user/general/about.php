    <h2 class="section-title">Chàng sinh viên và cú rẽ mang tên thời trang</h2>
    <p>Khoảng năm 2016, khi còn là sinh viên năm hai ngành Công nghệ Sinh học tại Đại học Tôn Đức Thắng, Nguyễn Trọng
        Lâm vẫn loay hoay với tương lai. Một bên là mong muốn có cuộc sống ổn định. Một bên là theo đuổi đam mê kinh
        doanh thời trang để làm đẹp cho mọi người.
        “Sau thời gian dài đấu tranh, Lâm lấy hết can đảm nói với gia đình”, anh chia sẻ. Bố mẹ nhận thấy con trai rất
        nghiêm túc với lĩnh vực kinh doanh nên ủng hộ bằng cách vay ngân hàng 200 triệu đồng, giúp anh khởi nghiệp. “Khi
        ấy, điều kiện phụ huynh đề ra cho Lâm là: làm việc tử tế và không bỏ cuộc dù trong bất kỳ hoàn cảnh nào”, anh
        cho biết.
        Sau khi quan sát và nghiên cứu thị trường, Trọng Lâm mở cửa hàng thời trang nam nhập khẩu đầu tiên mang tên
        L’Jin, với diện tích khoảng 20m2, vào tháng 4–2016. Nói về ý nghĩa tên gọi này, Lâm giải thích: “L bắt nguồn từ
        chữ cái đầu trong tên Lâm, còn Jin trong tiếng Hàn cũng có nghĩa là Lâm”.
        Vào giai đoạn mạng xã hội bùng nổ, L’Jin thành công tiếp cận đông đảo khách hàng trẻ thông qua Facebook. Sau sáu
        tháng hoạt động suôn sẻ, chàng trai sinh năm 1995 nâng tầm L’Jin sang không gian lớn hơn. Xuất phát từ ý định
        lấp đầy tầng lầu phía trên cửa hàng L’Jin mới, Trọng Lâm mở thêm một thương hiệu thời trang nữ nhập khẩu vào năm
        2017.
    </p>
    <p><strong>Đó là lúc LSOUL ra đời.</strong></p>
    <p>Sự phát triển của LSOUL được đánh dấu bởi <strong>ba cột mốc quan trọng</strong>. Trong ba năm đầu vận hành
        thương hiệu, từ 2017 đến 2020, nhắc đến thời trang nữ nhập khẩu châu Á, mọi người thường nghĩ đến LSOUL.
        COVID-19 khiến thương hiệu rơi vào khủng hoảng. Lúc này, đối sách duy nhất mà Nguyễn Trọng Lâm đưa ra nhằm duy
        trì thương hiệu là chuyển hướng sang các thiết kế nội địa.
        Khi đại dịch kết thúc, với tham vọng đưa LSOUL bước ra khỏi Việt Nam, tiến vào thị trường tiềm năng như Thái
        Lan, Hàn Quốc, Mỹ và châu Âu, nhà sáng lập “một lần nữa chuyển đổi mô hình kinh doanh sang thời trang thiết kế
        ready-to-wear”. Từ năm 2022 đến nay, LSOUL hoàn thiện đội ngũ thiết kế để cho ra đời mỗi tháng một bộ sưu tập,
        với các trang phục tiên phong trong kiểu dáng và xu hướng.
    </p>
